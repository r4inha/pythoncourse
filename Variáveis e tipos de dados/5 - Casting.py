# "Casting" é a conversão de tipos de variável#

# int
x = int(2)
y = int(2.8)
z = int('2')

print (x, y, z)

#-------------------------------#

# float
a = float (2.3)
b = float (2)
c = float ('2.4')
d = float ('2')

print (a, b, c, d)

#-------------------------------#

# str ou string
t = 's1'
r = 2
s = 2.3

print('A variável T é do tipo: ',type(t))
print('A variável R é do tipo: ',type(r))
print('A variável S é do tipo: ',type(s))